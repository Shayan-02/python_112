def sumNumbers(a: int, b: int) -> int:
    """
    This function gets two numbers and return sum of them.

    Args:
        a (int): first number
        b (int): second number

    Returns:
        int: sum of num1 and num2
    """
    return a + b


num1, num2 = 10, 20
print(sumNumbers(num1, num2))